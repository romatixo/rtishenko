package ru.job4j.generic;
/**
 * Base абстрактный класс.
 * @author romatihsenko
 * @since 5.07.2017
 * @version 1
 */
public abstract class Base {

    /**
     * getId - getter.
     * @return id.
     */
    abstract String getId();

    /**
     * setId - set id.
     * @param id - id.
     */
    abstract void setId(String id);
}
